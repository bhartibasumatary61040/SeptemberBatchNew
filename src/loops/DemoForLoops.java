package loops;

public class DemoForLoops {
    public static void main(String[] args) {
        for(int i =1; i<10; i++){
            System.out.println("INCREMENT LOOP:  Welcome to JAVA program  "+i);
        }

      for  ( int i=10; i>=1; i--){
          System.out.println(" DECREMENT LOOP ... : Welcome to java program "+i);

        }

    }
     
}
