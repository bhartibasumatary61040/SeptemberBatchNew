package loops;

public class DemoWhileLoop {
    public static void main(String[] args) {
     int i=0;
     while(i<5){
         System.out.println(" this is while loop "+i);
         i++;
     }
       i=7;
      while(i>1){
          System.out.println(" this is increment while loop "+i);
          i--;
      }

    }
}
